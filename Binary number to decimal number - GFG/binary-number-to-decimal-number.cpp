class Solution
{
	public:
		int binary_to_decimal(string str)
		{
		    // Code here.
		    int res=0;
		    int n=str.size();
		   
		    for(int i=0;i<n;i++)
		        res+=(1<<(n-i-1)) * (str[i]-'0');
		    return res;
		}
};

